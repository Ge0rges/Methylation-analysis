def _permute_p(stat_fn, data_a, data_b, n_perm: int, seed: int = 1) -> tuple[float, float]:
    """Return (observed_stat, permutation_p)."""
    rng = np.random.default_rng(seed)
    obs = stat_fn(data_a, data_b)
    pool = np.concatenate([data_a, data_b])
    n_a = len(data_a)
    more_extreme = 0
    for _ in range(n_perm):
        rng.shuffle(pool)
        perm_a, perm_b = pool[:n_a], pool[n_a:]
        more_extreme += stat_fn(perm_a, perm_b) >= obs
    pval = (more_extreme + 1) / (n_perm + 1)   # add-one smoothing
    return obs, pval


def _ks_bootstrap(data1, data2, n_permutations: int, seed: int) -> tuple[float, tuple]:
    """
    Performs a two-sample permutation test based on the Kolmogorov-Smirnov statistic.

    This implements Stephen's null hypothesis: that both samples are drawn from the
    same underlying distribution. It calculates the KS statistic (D) for the observed
    data and compares it to a distribution of KS statistics generated by repeatedly
    permuting the combined data and splitting it back into two samples.

    Args:
        data1 (array-like): First sample data.
        data2 (array-like): Second sample data.
        n_permutations (int): The number of permutations to perform.

    Returns:
        float: The empirical p-value based on the permutations.
            Returns None if either sample has fewer than 2 data points.
    """
    np.random.seed(seed)  # Ensure reproducibility

    # Convert to numpy arrays for easier handling
    data1 = np.asarray(data1)
    data2 = np.asarray(data2)

    # KS test requires at least 2 points in each sample for meaningful comparison often,
    # although ks_2samp might handle 1. Let's stick to the original check.
    if len(data1) < 5 or len(data2) < 5:
        # Cannot perform meaningful KS test
        print("Unexpected data size for KS test. Returning None.")
        return None, None

    # Calculate the observed KS statistic (D)
    # ks_2samp returns a result object (or tuple in older scipy)
    # The first element or the .statistic attribute is the D value.
    observed_ks_value = stats.ks_2samp(data1, data2).statistic
    
    if np.isnan(observed_ks_value) or np.isinf(observed_ks_value):
        # If the observed KS value is NaN or infinite, we cannot proceed
        print("Observed KS value is NaN or infinite. Returning None.")
        return None, None

    # Combine the data for permutation
    combined_data = np.concatenate((data1, data2))
    n1 = len(data1)
    
    # Cap permutations
    n_permutations = min(math.comb(len(combined_data), n1), n_permutations)

    count_extreme = 0
    for _ in range(n_permutations):
        # Permute the combined data
        np.random.shuffle(combined_data)
        
        # Split into two samples
        permuted_sample1 = combined_data[:n1]
        permuted_sample2 = combined_data[n1:]

        # Calculate KS statistic for the permuted samples
        perm_ks_value = stats.ks_2samp(permuted_sample1, permuted_sample2).statistic

        if perm_ks_value >= observed_ks_value:
            count_extreme += 1

    return (count_extreme + 1) / (n_permutations + 1), observed_ks_value


def _per_site_test(site_counts_a_flat, site_counts_b_flat):
    """
    Dispatch appropriate test based on replicate structure.


    Parameters
    ----------
    site_counts_a_flat, site_counts_b_flat : array-like
        Flat arrays of length 2*n_replicates


    Returns
    -------
    tuple
        (p_value, test_method, n_replicates_a, n_replicates_b)
    """
    try:
        n_replicates_a = len(site_counts_a_flat) // 2
        n_replicates_b = len(site_counts_b_flat) // 2


        # Single replicate case
        if n_replicates_a == 1 and n_replicates_b == 1:
            ma, ua = site_counts_a_flat[0], site_counts_a_flat
            mb, ub = site_counts_b_flat, site_counts_b_flat
            table = np.array([[ma, ua], [mb, ub]])
            try:
                res = stats.chi2_contingency(table)
                return res.pvalue, "chi2_single", n_replicates_a, n_replicates_b
            except Exception:
                return 1.0, "chi2_failed", n_replicates_a, n_replicates_b


        # Multiple replicate case
        else:
            # Build within-group tables
            a_table = np.array(site_counts_a_flat).reshape(n_replicates_a, 2)
            b_table = np.array(site_counts_b_flat).reshape(n_replicates_b, 2)


            # Test for consistency within replicates: 2xN table for each group
            try:
                a_pval = stats.chi2_contingency(a_table).pvalue if n_replicates_a > 1 else 1.0
                b_pval = stats.chi2_contingency(b_table).pvalue if n_replicates_b > 1 else 1.0
            except Exception:
                return 1.0, "replicates_test_failed", n_replicates_a, n_replicates_b


            # If replicates differ within A or B, reject test, no pooling
            if a_pval < 0.05 or b_pval < 0.05:
                return 1.0, "replicates_inconsistent", n_replicates_a, n_replicates_b


            # Pool replicates for A and B
            pooled_a = np.sum(a_table, axis=0)
            pooled_b = np.sum(b_table, axis=0)
            table = np.array([pooled_a, pooled_b])


            try:
                pval = stats.chi2_contingency(table).pvalue
                return pval, "chi2_pooled", n_replicates_a, n_replicates_b
            except Exception:
                return 1.0, "chi2_failed", n_replicates_a, n_replicates_b


    except Exception:
        return 1.0, "failed", 0, 0

    
