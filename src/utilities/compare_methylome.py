def compare_methylomes(
    beta_a: np.ndarray,
    beta_b: np.ndarray,
    counts_a: pl.DataFrame | None = None,
    counts_b: pl.DataFrame | None = None,
    *,
    n_perm: int = 10_000,
    n_boot: int = 5_000,
    alpha: float = 0.05,
    seed: int = 1,
    motif: Motif = None
) -> dict:
    """
    Compare two methylomes with flat 2D count arrays.
    
    Parameters
    ----------
    beta_a, beta_b : array-like
        Methylation fractions for samples A and B
    counts_a, counts_b : array-like, optional
        Count arrays with shape (n_sites, 2*n_replicates):
        - (n_sites, 2): Single replicate -> [[meth, unmeth], ...]
        - (n_sites, 4): Two replicates -> [[meth1, unmeth1, meth2, unmeth2], ...]
        - (n_sites, 2*n): n replicates -> [[meth1, unmeth1, ..., methn, unmethn], ...]
    n_perm : int, default 10000
        Number of permutations for global tests
    n_boot : int, default 5000
        Number of bootstrap samples for KS confidence interval
    alpha : float, default 0.05
        Significance threshold for FDR correction
    seed : int, default 1
        Random seed for reproducibility
    
    Returns
    -------
    dict
        Dictionary with keys 'global_table' (str) and 'per_site_df' (DataFrame)
    """
    beta_a = np.asarray(beta_a, dtype=float)
    beta_b = np.asarray(beta_b, dtype=float)
    assert beta_a.shape == beta_b.shape, "Beta arrays must be same length"

    # --------------------------------------------------------------
    # 1. Global tests
    # --------------------------------------------------------------
    # (a) Bootstrapped KS
    p_value, d_value = _ks_bootstrap(beta_a, beta_b, n_boot, seed)

    # (b) Wasserstein distance (W1)
    w1_stat, w1_p = _permute_p(stats.wasserstein_distance, beta_a, beta_b, n_perm, seed)

    # (c) Energy distance
    ed_stat, ed_p = _permute_p(stats.energy_distance, beta_a, beta_b, n_perm, seed)

    # (d) Epps-Singleton
    es_res = stats.epps_singleton_2samp(beta_a, beta_b)

    # Assemble global table
    global_table = pl.DataFrame({
        "Test": [
            "Kilmogorov-Smirnov",
            "1-Wasserstein",
            "Energy distance",
            "Epps-Singleton χ²"
        ],
        "Statistic": [
            d_value,
            w1_stat,
            ed_stat,
            es_res.statistic
        ],
        "p-value": [
            p_value,
            w1_p,
            ed_p,
            es_res.pvalue
        ],
        "Notes": [
            "non-parametric; bootstrapped",
            "interpretable in β-units",
            "metric; EDF-weighted",
            "robust to ties"
        ]
    })
    
    # Assemble human-readable table
    global_table_str = (
        "| Test | Statistic | p-value | Notes |\n"
        "|---|---|---|---|\n"
        f"| KS D (bootstrap) | {d_value:.4f} | {p_value:.4g} | non-parametric |\n"
        f"| 1-Wasserstein | {w1_stat:.4f} | {w1_p:.4g} | interpretable in β-units |\n"
        f"| Energy distance | {ed_stat:.4f} | {ed_p:.4g} | metric; EDF-weighted |\n"
        f"| Epps-Singleton χ² | {es_res.statistic:.2f} | {es_res.pvalue:.4g} | robust to ties |"
    )

    # --------------------------------------------------------------
    # 2. Per-site tests with flat 2D count array parsing
    # --------------------------------------------------------------
    if counts_a is not None and counts_b is not None:    
        pvals = []
        test_methods = []
        n_replicates_a_list = []
        n_replicates_b_list = []
        contigs = []
        strands = []
        positions = []
        
        for site_counts_a, site_counts_b in zip(counts_a.group_by("contig", "strand", "position", maintain_order=True), counts_b.group_by("contig", "strand", "position", maintain_order=True)):
            site_counts_a_df = (site_counts_a[1].with_columns(pl.struct("contig", "strand", "position").cum_count().over(pl.struct("contig", "strand", "position")).alias("replicate"))
                                .pivot(
                                    values=[motif.meth_type, motif.canonical_base],
                                    index=["contig", "strand", "position"],
                                    columns="replicate"
                                ))

            replicates = sorted(set(int(c.split('_')[-1]) for c in site_counts_a_df.columns if '_' in c))
            desired_cols = [f"{v}_{r}" for r in replicates for v in [motif.meth_type, motif.canonical_base] if f"{v}_{r}" in site_counts_a_df.columns]
            site_counts_a_df = site_counts_a_df.select(desired_cols).to_numpy()
            
            site_counts_b_df = (site_counts_b[1].with_columns(pl.struct("contig", "strand", "position").cum_count().over(pl.struct("contig", "strand", "position")).alias("replicate"))
                                .pivot(
                                    values=[motif.meth_type, motif.canonical_base],
                                    index=["contig", "strand", "position"],
                                    columns="replicate"
                                ))

            replicates = sorted(set(int(c.split('_')[-1]) for c in site_counts_b_df.columns if '_' in c))
            desired_cols = [f"{v}_{r}" for r in replicates for v in [motif.meth_type, motif.canonical_base] if f"{v}_{r}" in site_counts_b_df.columns]
            site_counts_b_df = site_counts_b_df.select(desired_cols).to_numpy()
            
            pval, method, n_rep_a, n_rep_b = _per_site_test(site_counts_a_df[0], site_counts_b_df[0])
            pvals.append(pval)
            test_methods.append(method)
            n_replicates_a_list.append(n_rep_a)
            n_replicates_b_list.append(n_rep_b)
            contig, strand, position = site_counts_a[0][0], site_counts_a[0][1], site_counts_a[0][2]
            contigs.append(contig)
            strands.append(strand)
            positions.append(position)
            
        # FDR correction
        pvals = np.array(pvals)
        reject, qvals, *_ = multipletests(pvals, alpha=alpha, method="fdr_tsbh")
        
        per_site_df = pl.from_dict({
            "contig": contigs,
            "strand": strands,
            "position": positions,
            "beta_A": beta_a,
            "beta_B": beta_b,
            "n_replicates_A": n_replicates_a_list,
            "n_replicates_B": n_replicates_b_list,
            "test_method": test_methods,
            "p_raw": pvals,
            "q_BH": qvals,
            "significant": reject,
        })
    else:
        per_site_df = None  # empty frame when no counts provided

    return {"global_table": global_table, "global_table_str": global_table_str, "per_site_df": per_site_df}
