@cli.command(short_help="Analyze genome methylation data.")
@click.option("--fna-file", "-f", required=True, type=click.Path(exists=True), help="Path to the .fna file of the genome.")
@click.option("--barcode-map-file", "-b", required=True, type=click.Path(exists=True), help="TSV mapping barcodes to treatments and samples WITH HEADER (e.g. 'barcode\ttreatment\tsample').")
@click.option("--methylation-data-dir", "-m", required=True, type=click.Path(exists=True), help="Directory containing pileup .bed files for each genome.")
@click.option("--output-dir", "-o", required=True, type=click.Path(), help="Directory to store the analysis results (tables, plots, processed data, etc.).")
@click.option("--coverage", "-c", default=5, show_default=True, help="Minimum coverage threshold for analysis.")
@click.option("--gene-calls", "-g", default="gene-calls.txt", show_default=True, help="Path to gene call file output by anvi'o.")
@click.option("--function-calls", "-u", default="function-calls.txt", show_default=True, help="Path to function annotations file output by anvi'o.")
@click.option("--treatments", "-t", required=True, multiple=True, help="Treatments to be compared must define at least two (e.g. -t top -t bottom).")
@click.option("--treatment-info", "-i", required=True, type=click.Path(exists=True), help="TSV file containing treatment information WITH HEADER. (e.g. 'treatment\treadable_treatment\tcolor\torder').")
def analyze_genome(
    fna_file: str,
    barcode_map_file: str,
    methylation_data_dir: str,
    output_dir: str,
    coverage: int,
    gene_calls: str,
    function_calls: str,
    treatments: list[str],
    treatment_info: str
):
    """
    Analyze methylation data generated by the do_methylate script.

    This command:
      1) Loads the genome (FASTA) and the pre-generated .bed files from do_methylate.
      2) Loads barcode→treatment mappings.
      3) Applies any coverage-based filtering, merges or processes the .bed data as needed.
      4) Optionally identifies motifs or does any domain-specific analysis.
      5) Generates plots or final output files.

    Use --meta to indicate metagenomic data analysis instead of single-genome analysis.
    """

    # Convert click paths to Path objects
    fna_file = Path(fna_file)
    barcode_map_file = Path(barcode_map_file)
    methylation_data_dir = Path(methylation_data_dir)
    output_dir = Path(output_dir)
    gene_calls = Path(gene_calls)
    function_calls = Path(function_calls)
    treatment_info = Path(treatment_info)

    # Ensure output directories exist
    output_dir.mkdir(parents=True, exist_ok=True)
    
    genome = Genome(fna_file, methylation_data_dir, gene_calls, function_calls, barcode_map_file, output_dir, treatments, coverage, treatment_info)
    
    # If this genome has Motif objects, proceed:
    motifs = genome.motifs
    if not motifs:
        click.echo("No motifs available to analyze.")
        return

    for motif in motifs:
        click.echo(f"Analyzing motif: {motif.motif} (meth_type={motif.meth_type})")

        # Whole methylome plot for this motif type
        plot_whole_methylome(genome, motif, output_dir)

        # Motif-level methylation across treatments
        plot_motif_methylation_distribution(genome, motif, output_dir)

        # DMR scores heatmap
        plot_dmr_scores_heatmap(genome, motif, output_dir)

        # Parallel categories
        plot_parallel_categories_methylation(genome, motif, output_dir, bins=3)

        # Extract top diff methylated genes
        trans = extract_motif_data_all_transitions(genome, motif)
        dmrs = extract_diff_methylated_genes(genome, motif, top_n=0)
        if dmrs is not None and trans is not None:
            extract_consensus_genes(genome, trans, dmrs, motif)
        
    # Basic stats file
    write_basic_stats(genome, motifs)

    click.echo("Analysis complete.")


